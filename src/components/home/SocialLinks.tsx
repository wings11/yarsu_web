// Social media icons mapping
const socialIcons: Record<string, { icon: React.ComponentType; color: string }> = {
  'facebook': { icon: FacebookIcon, color: 'text-blue-600' },
  'instagram': { icon: InstagramIcon, color: 'text-pink-600' },
  'twitter': { icon: TwitterIcon, color: 'text-blue-400' },
  'tiktok': { icon: TikTokIcon, color: 'text-black' },
  'youtube': { icon: YouTubeIcon, color: 'text-red-600' },
  'line': { icon: LineIcon, color: 'text-green-500' },
  'whatsapp': { icon: WhatsAppIcon, color: 'text-green-600' },
  'telegram': { icon: TelegramIcon, color: 'text-blue-500' },
  'linkedin': { icon: LinkedInIcon, color: 'text-blue-700' },
  'website': { icon: () => <Globe className="w-5 h-5" />, color: 'text-gray-600' },
}

export default function SocialLinks() {
  const { data: links, isLoading, error } = useLinks()

  if (isLoading || error || !links || links.length === 0) {
    return null // Hide if no links or loading
  }

  return (
    <div className="flex items-center space-x-4">
      <span className="text-sm text-gray-600 font-medium">Follow us:</span>
      <div className="flex items-center space-x-3">
        {links.map((link: Link) => {
          const platformKey = link.platform.toLowerCase().replace(/\s+/g, '')
          const socialIcon = socialIcons[platformKey] || socialIcons['website']
          const IconComponent = socialIcon.icon
          
          return (
            <a
              key={link.id}
              href={link.url}
              target="_blank"
              rel="noopener noreferrer"
              className={`inline-flex items-center justify-center w-10 h-10 rounded-full bg-gray-100 hover:bg-gray-200 transition-colors ${socialIcon.color}`}
              title={`Visit our ${link.platform}`}
            >
              <IconComponent />
            </a>
          )
        })}
      </div>
    </div>
  )
}

// Alternative text-based version for header
export function SocialLinksText() {
  const { data: links, isLoading, error } = useLinks()

  if (isLoading || error || !links || links.length === 0) {
    return null
  }

  return (
    <div className="flex items-center space-x-4">
      {links.map((link: Link) => (
        <a
          key={link.id}
          href={link.url}
          target="_blank"
          rel="noopener noreferrer"
          className="flex items-center space-x-1 text-sm text-gray-600 hover:text-primary-600 transition-colors"
        >
          <ExternalLink className="h-4 w-4" />
          <span>{link.platform}</span>
        </a>
      ))}
    </div>
  )
}
